tabdata<-read.xlsx("Data.xlsx",sheetIndex = 2)
tabdata=ts(tabdata[,2],start=c(2017,1),end=c(2017,12),frequency = 12)
plot(tabdata,xlab="Months",ylab="Tablets Sales")
