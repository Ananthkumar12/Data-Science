Injdata<-read.xlsx("Data.xlsx",sheetIndex = 7)
Injdata=ts(Injdata[,2],start=c(2017,1),end=c(2017,12),frequency = 12)
plot(Injdata,xlab="Months",ylab="Injection Sales")
plot(diff(Injdata),ylab="Differenced Injection sales")
